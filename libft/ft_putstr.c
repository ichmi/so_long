#include "libft.h"

int	ft_putstr(char *s)
{
	int	bw;

	if (!s)
		return (write(1, "(null)", 6));
	bw = 0;
	while (*s)
	{
		bw += ft_putchar(*s);
		s++;
	}
	return (bw);
}
